# Description: This program calculates the circumference and area of a circle based on the input radius

radius = float(input("Enter the radius of the circle")) #prompt

circumference = 2 * 3.14159 * radius # Calculate the circumference and area of the circle
area = 3.14 * radius * 2

print("The circumference of the circle is", circumference) # result
print("The area of the circle is", area)
